
public class Endereco {
	private String meuEndereco;
	private String fiapEndereco;
	private String voEndereco;
	
	public Endereco(String meuEndereco1, String fiapEndereco1, String voEndereco1)
	{
		meuEndereco = meuEndereco1;
		fiapEndereco = fiapEndereco1;
		voEndereco = voEndereco1;
	}
	public String getmeuEndereco() {
		return meuEndereco;
	}

	public String getfiapEndereco() {
		return fiapEndereco;
	}
	
	public String getvoEndereco() {
		return voEndereco;
	}

}
